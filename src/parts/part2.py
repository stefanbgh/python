# Strings
print("------------------------")

food = "pizza"
email = "example@gmail.com"

print(f"I love {food}!")
print(f"My email is: {email}")

print("------------------------")
# Integers
print("------------------------")

age = 24

print(f"I'm {age} years old.")

print("------------------------")
# Float
print("------------------------")

price = 10.99

print(f"The price is ${price}")

print("------------------------")
# Boolean
print("------------------------")
is_student = False
question = "Are you a student?"

if is_student:
    print(f"{question} Yes")
else:
    print(f"{question} No")

print("------------------------")